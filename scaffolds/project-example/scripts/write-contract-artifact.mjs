#!/usr/bin/env node
/**
 * write-contract-artifact.mjs
 *
 * Generates dapp/src/generated/contracts-addresses.ts from deployment tracking.
 *
 * Priority (highest first):
 *   1. CONTRACT_ADDRESSES_FILE env — path to a JSON address map (for CI/custom overrides)
 *   2. EXAMPLE_COUNTER_ADDRESS + EXAMPLE_COUNTER_CHAIN_ID env — single-contract override
 *   3. deployments/contracts.json — written by deploy scripts; source of truth for all networks
 *
 * When none of the above are set the output is an empty map — meaning "not deployed yet".
 * Run `pnpm deploy` (local) or `pnpm deploy:testnet` to populate contracts.json.
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ARTIFACT_PATH = resolve(__dirname, '..', 'dapp', 'src', 'generated', 'contracts-addresses.ts');
const TRACKING_PATH = resolve(__dirname, '..', 'deployments', 'contracts.json');

function toAddressMapFromEnv() {
  // 1. Explicit JSON file override
  const filePathRaw = process.env.CONTRACT_ADDRESSES_FILE?.trim();
  if (filePathRaw) {
    const filePath = filePathRaw.startsWith('/')
      ? filePathRaw
      : resolve(__dirname, '..', filePathRaw);
    if (!existsSync(filePath)) {
      throw new Error(`CONTRACT_ADDRESSES_FILE does not exist: ${filePath}`);
    }
    try {
      const parsed = JSON.parse(readFileSync(filePath, 'utf8'));
      if (parsed && typeof parsed === 'object') return parsed;
    } catch {
      throw new Error('Invalid CONTRACT_ADDRESSES_FILE: expected a JSON object map');
    }
  }

  // 2. Deployment tracking file — written by deploy scripts
  if (existsSync(TRACKING_PATH)) {
    try {
      const tracking = JSON.parse(readFileSync(TRACKING_PATH, 'utf8'));
      const networks = tracking?.networks;
      if (networks && typeof networks === 'object') {
        const byChain = {};
        for (const value of Object.values(networks)) {
          const chainId = Number(value?.chainId);
          if (!Number.isFinite(chainId) || chainId <= 0) continue;
          if (!byChain[chainId]) byChain[chainId] = {};
          const contracts = value?.contracts ?? {};
          for (const [name, entry] of Object.entries(contracts)) {
            if (!entry || typeof entry.address !== 'string') continue;
            byChain[chainId][name] = entry.address;
          }
        }
        // Return whatever is tracked, even if empty — do NOT fall back to a stale file
        return byChain;
      }
    } catch {
      // Malformed tracking file — fall through
    }
  }

  // 3. Single-contract env-var override (used by Docker / CI)
  const exampleAddress = process.env.EXAMPLE_COUNTER_ADDRESS?.trim();
  if (exampleAddress) {
    const chainId = Number(process.env.EXAMPLE_COUNTER_CHAIN_ID ?? '2030');
    if (!Number.isFinite(chainId) || chainId <= 0) {
      throw new Error('Invalid EXAMPLE_COUNTER_CHAIN_ID: must be a positive number');
    }
    return { [chainId]: { ExampleCounter: exampleAddress } };
  }

  // No deployment data → empty map (dapp will show "not deployed" state)
  return {};
}

function buildArtifact(map) {
  return `export type ContractAddressMap = Record<number, Record<string, \`0x\${string}\`>>;\n\n/**\n * Generated by scripts/write-contract-artifact.mjs\n *\n * Shape:\n * {\n *   [chainId]: {\n *     [contractName]: address\n *   }\n * }\n *\n * Populated by: pnpm deploy          (local)\n *               pnpm deploy:testnet  (testnet)\n *               pnpm deploy:mainnet  (mainnet)\n */\nexport const CONTRACT_ADDRESSES_BY_CHAIN_ID: ContractAddressMap = ${JSON.stringify(map, null, 2)} as ContractAddressMap;\n\nexport function getContractAddress(chainId: number, contractName: string): \`0x\${string}\` | undefined {\n  return CONTRACT_ADDRESSES_BY_CHAIN_ID[chainId]?.[contractName];\n}\n`;
}

function main() {
  const map = toAddressMapFromEnv();
  mkdirSync(resolve(__dirname, '..', 'dapp', 'src', 'generated'), { recursive: true });
  writeFileSync(ARTIFACT_PATH, buildArtifact(map), 'utf8');
  const chainCount = Object.keys(map).length;
  if (chainCount === 0) {
    console.log('No deployments found — wrote empty address map.');
    console.log('Run `pnpm deploy` to deploy locally, or `pnpm deploy:testnet` for testnet.');
  } else {
    console.log(`Wrote contract addresses for ${chainCount} network(s): ${ARTIFACT_PATH}`);
  }
}

main();
